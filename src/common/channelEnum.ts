export class Action {
    /**
     * open new window.
     * @type {string}
     */
    static NewWin: string = 'new-win';
    /**
     * close current window.
     * @type {string}
     */
    static CloseWin: string = 'close-win';
    /**
     * new tab in current window.
     * @type {string}
     */
    static NewTab: string = 'new-tab';


}